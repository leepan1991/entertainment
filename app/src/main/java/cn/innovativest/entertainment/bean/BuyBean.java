package cn.innovativest.entertainment.bean;

public class BuyBean {
    private String card_title;
    private String card_day;
    private String card_fee;
    private String card_credit;

    public String getCard_title() {
        return card_title;
    }

    public void setCard_title(String card_title) {
        this.card_title = card_title;
    }

    public String getCard_day() {
        return card_day;
    }

    public void setCard_day(String card_day) {
        this.card_day = card_day;
    }

    public String getCard_fee() {
        return card_fee;
    }

    public void setCard_fee(String card_fee) {
        this.card_fee = card_fee;
    }

    public String getCard_credit() {
        return card_credit;
    }

    public void setCard_credit(String card_credit) {
        this.card_credit = card_credit;
    }
}
